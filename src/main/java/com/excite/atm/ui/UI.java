    private void depositActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositActionActionPerformed
        try {
            Map<NoteType, Integer> depositAmount = new HashMap<>();
            NoteType noteType = (NoteType) noteTypeDepositCombobox.getSelectedItem();
            String depositAmountString = this.depositAmount.getText();
            BigDecimal amount = new BigDecimal(depositAmountString);
            depositAmount.put(noteType, amount.intValue());
            UI.dispenser.deposit(depositAmount);
            outputDepositTextArea.setText(UI.dispenser.printMoney());
            this.depositAmount.setText("");
        } catch (Exception e) {
            outputDepositTextArea.setText(e.getMessage());
        }

    }//GEN-LAST:event_depositActionActionPerformed


    private void withdrawActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawActionActionPerformed
        String amountText = withdrawAmount.getText();

        try {
            BigDecimal amount = new BigDecimal(amountText);
            Map<NoteType, Integer> result = dispenser.dispensing(amount);
            StringBuilder builder = new StringBuilder();

            Formatter fmt = new Formatter(builder);
            fmt.format("Total Note has been withdraw.");
            for (Map.Entry<NoteType, Integer> entry : result.entrySet()) {
                if (entry.getValue() != 0) {
                    fmt.format("%s = %s%n", entry.getKey().toString(), entry.getValue());
                }
            }
            outputTextArea.setText(builder.toString());
        } catch (Throwable e) {
            outputTextArea.setText(e.getMessage());
        }

        withdrawAmount.setText("");

    }//GEN-LAST:event_withdrawActionActionPerformed

